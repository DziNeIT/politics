package pw.ollie.politicswar.event.war;

import pw.ollie.politics.group.Group;
import pw.ollie.politicswar.war.War;

import org.bukkit.event.Cancellable;
import org.bukkit.event.HandlerList;

public class WarFinishEvent extends WarEvent implements Cancellable {
    private static final HandlerList handlers = new HandlerList();

    private boolean cancelled;

    public WarFinishEvent(War war) {
        super(war);
    }

    public Group getWinner() {
        return getWar().getWinningGroup();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean isCancelled() {
        return cancelled;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void setCancelled(boolean cancelled) {
        this.cancelled = cancelled;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public HandlerList getHandlers() {
        return handlers;
    }

    public static HandlerList getHandlerList() {
        return handlers;
    }
}
